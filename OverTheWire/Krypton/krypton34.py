def count_common_letters(f, groupsize):

    letter_count = {}
    for i in range(len(f)):
        if (i+groupsize) < len(f):
            letter_count[f[i:i+groupsize]] = letter_count.get(f[i:i+groupsize], 0) + 1

    return letter_count

def print_dict(d, limit=30):
    idx = 0
