def check_rotation(dec_word, enc_word):
    letters_offsets = []
    for i in range(len(dec_word)):
        offset = (alphabet.index(dec_word[i]) - alphabet.index(enc_word[i])) % len(alphabet)
        letters_offsets.append(offset)
    
    return letters_offsets

